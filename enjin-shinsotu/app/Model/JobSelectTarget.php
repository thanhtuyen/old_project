	/**
	* 目標数の取得
	* 
	* @param  int   $rec_company_id
	* @param  array   $data
	* @return array $result
	* 
	**/
	public function getTargetNumber( $rec_company_id, $data )
	{
		$param = array( $rec_company_id, $data['year'] );

		$sql = " SELECT job_select_targets.job_vote_id, 
						job_select_targets.target_number, 
					    job_select_targets.attain_deadline_date, 
       					job_select_targets.screening_stage_id, 
       					job_select_targets.select_judgment_type, 
       					job_select_targets.target_select_id, 
       					screening_stages.name 
				 FROM job_select_targets				 
				 INNER JOIN screening_stages ON screening_stages.id = job_select_targets.screening_stage_id 
				 INNER JOIN job_votes ON job_votes.id = job_select_targets.job_vote_id 
				 INNER JOIN rec_departments ON job_votes.rec_department_id = rec_departments.id 				 
				 WHERE rec_departments.rec_company_id = ? 
				 AND job_select_targets.status = 0 
				 AND job_votes.status = 0 
				 AND rec_departments.status = 0 
				 AND job_votes.wanted_year = ? 
			 	 ORDER BY job_select_targets.screening_stage_id, 
				          job_select_targets.target_select_id, 
				          job_select_targets.attain_deadline_date";

		$result = $this->query( $sql, $param );


		return $result;

	}
